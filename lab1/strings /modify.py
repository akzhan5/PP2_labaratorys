#str.upper()
a = 'Hello, world'
print(a.upper())

#str.lower()
print(a.lower())

#remove whitespace - space before or after the actual text str.strip()
a = "   Hello, World!   " 
print(a.strip())

#replace string - str.replace(c1, c2)
a = 'Hello, World!'
print(a.replace('H', 'J'), a.replace('!', ')')) #Jello, World)

#split string 
a = 'Hello, World!'
print(a.split(',')) #splits into an array after eahc given instance






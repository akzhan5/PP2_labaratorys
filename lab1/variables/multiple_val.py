
#Many values to Multiple Variables
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)

#one val to mult vars
x = y = z = "Orange"
print(x)
print(y)
print(z)

#unpack collection - list [], set{}, tuple()
fruits = {"apple", "banana", "cherry"}
x, y, z = fruits
print(x)
print(y)
print(z)